using System;
using System.Collections.Generic;
using System.Text;

namespace Calculadora
{
    class Calculo
    {
        public int N1;
        public int N2;
        
        public int Adicao()
        {
            return  N1 + N2;
        }
        public int Subtracao()
        {
            return N1 - N2;
        }
        public int Multiplicacao()
        {
            return N1 * N2;
        }
        public int Divisao()
        {
            return N1 - N2;
        }

    }
}

